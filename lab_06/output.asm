EXTRN TOSHEX : NEAR
EXTRN TOUBIN : NEAR
EXTRN SHEX : BYTE
EXTRN UBIN : BYTE
EXTRN FLAG : BYTE

PUBLIC CRLF
PUBLIC OUTUHEX
PUBLIC OUTSBIN

DATASEG SEGMENT PARA PUBLIC 'DATA'
    OUTUHEXMSG DB 'Signed hexadecimal number: $'
    OUTSBINMSG DB 'Unigned binary number: $'
DATASEG ENDS

CODESEG SEGMENT PARA PUBLIC 'CODE'
    ASSUME CS:CODESEG

CRLF PROC NEAR
    MOV AH, 2
    MOV DL, 13
    INT 21H
    MOV DL, 10
    INT 21H
    RET
CRLF ENDP

OUTUHEX PROC NEAR
    MOV AH, 9
    MOV DX, OFFSET OUTUHEXMSG
    INT 21H

    CALL TOSHEX

    MOV AH, 2
    MOV DL, FLAG
    INT 21H

    MOV AH, 9
    MOV DX, OFFSET SHEX
    INT 21H

    CALL CRLF
    CALL CRLF

    RET
OUTUHEX ENDP

OUTSBIN PROC NEAR
    MOV AH, 9
    MOV DX, OFFSET OUTSBINMSG
    INT 21H

    CALL TOUBIN

    

    MOV AH, 9
    MOV DX, OFFSET UBIN
    INT 21H

    CALL CRLF
    CALL CRLF

    RET
OUTSBIN ENDP

CODESEG ENDS
END